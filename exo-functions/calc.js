const add = (n1, n2) => {
  return n1 + n2;
};

const sub = (n1, n2) => {
  return n1 - n2;
};

const mul = (n1, n2) => {
  return n1 * n2;
};

const div = (n1, n2) => {
  return n1 / n2;
};

