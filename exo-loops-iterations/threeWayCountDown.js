let countdown = 101;
console.log(`\Countdown de for`);
for (countdown; countdown > 0; countdown -= 2) {
  console.log(countdown);
}

let count2 = 101;
console.log(`\Countdown de While`);
while (count2 > 0) {
  console.log(count2);
  count2 -= 2;
}

let count3 = 101;
console.log(`\Countdown de do-While`);
do {
  console.log(count3);
  count3 -= 2;
} while (count3 > 0);
